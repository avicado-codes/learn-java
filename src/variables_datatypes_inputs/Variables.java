package variables_datatypes_inputs;
public class Variables {
    public static void main (String[] args){

        // Integer declaration and value assignment to variable
        int myNumber = 123;
        System.out.printf("The number stored is %d\n",myNumber);

        // Boolean datatype
        boolean isTrue = true;
        System.out.println(isTrue);

        // Float datatype
        float num = 5.0f;
        System.out.printf("The number stored is %.2f\n",num);

        String wishes = "Happy Birthday";
        System.out.println(wishes);
    }
}