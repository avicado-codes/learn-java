package variables_datatypes_inputs;

public class Datatypes {
    public static void main(String[] args) {
        // Primitive data types
        int myInt = 42;                  // Integer
        double myDouble = 3.14159;       // Floating point number
        char myChar = 'A';               // Single character
        boolean myBool = true;           // Boolean value
        long myLong = 123456789L;        // Long integer
        float myFloat = 9.81f;           // Float number
        short myShort = 32000;           // Short integer
        byte myByte = 127;               // Byte value

        // Reference data type
        String myString = "Hello, Java!";

        // Print them out
        System.out.println("Integer: " + myInt);
        System.out.println("Double: " + myDouble);
        System.out.println("Char: " + myChar);
        System.out.println("Boolean: " + myBool);
        System.out.println("Long: " + myLong);
        System.out.println("Float: " + myFloat);
        System.out.println("Short: " + myShort);
        System.out.println("Byte: " + myByte);
        System.out.println("String: " + myString);
    }
}
